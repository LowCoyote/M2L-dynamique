				<h3 class="titre">Accès Internet</h3>
				<p class="corps">Les ligues disposent d'un accès Internet mutualisé que la M2L loue à un prestataire extérieur.</p>
			</div>
			<div class="article">
				<a class="anchor" id="wifi"></a>	
				<h3  class="titre">Accès Wifi</h3>
				<p class="corps">Dans tous les espaces, un réseau Wifi "visiteurs" est disponible, avec une clé WPA renouvelée régulièrement et communiquée aux ligues. Ce réseau ne permet que l'accès à Internet.</p>
			</div>
			<div class="article">
				<a class="anchor" id="telephonie"></a>	
				<h3  class="titre">Téléphonie</h3>
				<p class="corps">Dans les bâtiments anciens A et B, les salles et bureaux sont équipés de prises de téléphone analogiques. La M2L y fournit les combinés téléphoniques. Dans les bâtiments neufs C et D, l'équipement téléphonique est de type VoIP. La M2L loue des postes téléphoniques IP aux ligues.</p>
			</div>
			<div class="article">
				<a class="anchor" id="affranchissement"></a>	
				<h3 class="titre">Affranchissement</h3>
				<p class="corps">Une machine à affranchir permet un affranchissement rapide et en nombre. Cette prestation est facturée aux ligues au coût de l'affranchissement. Chaque mois, on relie la machine à affranchir à une imprimante pour obtenir une liste de codes de gestion correspondant aux ligues associés à une quantité et un type d'affranchissement. La prise en compte de ces informations permet au CROSL d'éditer des factures.</p>
